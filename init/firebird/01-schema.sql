-- -------------------------------------------------------------
-- Tabla: alumnos
-- Diferencia Firebird: no existe IF NOT EXISTS → el reset
-- (reset.sh) elimina los volúmenes antes de volver a cargar.
-- -------------------------------------------------------------
CREATE TABLE alumnos (
    id        INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre    VARCHAR(60)  NOT NULL,
    apellido  VARCHAR(60)  NOT NULL,
    email     VARCHAR(100) UNIQUE,           -- igual que MariaDB/PostgreSQL
    fecha_nac DATE,
    ciudad    VARCHAR(60)
);

-- -------------------------------------------------------------
-- Tabla: cursos
-- -------------------------------------------------------------
CREATE TABLE cursos (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre      VARCHAR(100) NOT NULL,
    descripcion BLOB SUB_TYPE TEXT,           -- TEXT no existe en Firebird
    creditos    SMALLINT DEFAULT 3
);

-- -------------------------------------------------------------
-- Tabla: matriculas (relación alumno ↔ curso)
-- -------------------------------------------------------------
CREATE TABLE matriculas (
    id        INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    alumno_id INTEGER NOT NULL,
    curso_id  INTEGER NOT NULL,
    fecha_alta DATE DEFAULT CURRENT_DATE,
    CONSTRAINT fk_mat_alumno FOREIGN KEY (alumno_id) REFERENCES alumnos(id),
    CONSTRAINT fk_mat_curso  FOREIGN KEY (curso_id)  REFERENCES cursos(id),
    CONSTRAINT uq_matricula  UNIQUE (alumno_id, curso_id)
);

-- -------------------------------------------------------------
-- Tabla: notas
-- -------------------------------------------------------------
CREATE TABLE notas (
    id           INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    matricula_id INTEGER     NOT NULL,
    evaluacion   VARCHAR(50) NOT NULL,
    nota         NUMERIC(4,2),
    CONSTRAINT fk_nota_mat  FOREIGN KEY (matricula_id) REFERENCES matriculas(id),
    CONSTRAINT chk_nota     CHECK (nota >= 0 AND nota <= 10)
);

