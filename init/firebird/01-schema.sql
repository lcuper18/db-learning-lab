--
-- Diferencias clave respecto a MySQL/PostgreSQL:
--   • No existe IF NOT EXISTS en CREATE TABLE
--   • Los INSERT deben ir separados (no multi-row VALUES)
--   • Se requiere COMMIT explícito al finalizar
--   • BLOB SUB_TYPE TEXT en lugar de TEXT
--   • LIMIT no existe → usar ROWS N o FETCH FIRST N ROWS ONLY
--   • Concatenación: || (igual que PostgreSQL, distinto de CONCAT())
--   • Usuario siempre en MAYÚSCULAS (LABUSER, no labuser)
